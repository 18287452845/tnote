-- 大数据量表（用于生成慢查询）
DROP TABLE IF EXISTS access_log;
CREATE TABLE access_log (
    id INT PRIMARY KEY AUTO_INCREMENT,
    user_id INT,
    action VARCHAR(50),
    ip_address VARCHAR(45),
    access_time DATETIME,
    response_ms INT,
    url VARCHAR(200)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- 用存储过程批量插入 5000 条访问日志
DELIMITER //
DROP PROCEDURE IF EXISTS generate_access_log//
CREATE PROCEDURE generate_access_log()
BEGIN
    DECLARE i INT DEFAULT 0;
    WHILE i < 5000 DO
        INSERT INTO access_log (user_id, action, ip_address, access_time, response_ms, url)
        VALUES (
            FLOOR(1 + RAND() * 40),
            ELT(FLOOR(1 + RAND() * 5), 'login','query','update','delete','export'),
            CONCAT(FLOOR(RAND()*255),'.',FLOOR(RAND()*255),'.',FLOOR(RAND()*255),'.',FLOOR(RAND()*255)),
            DATE_SUB(NOW(), INTERVAL FLOOR(RAND() * 30) DAY),
            FLOOR(RAND() * 5000),
            ELT(FLOOR(1 + RAND() * 4), '/api/students','/api/scores','/api/login','/api/export')
        );
        SET i = i + 1;
    END WHILE;
END//
DELIMITER ;

CALL generate_access_log();
DROP PROCEDURE generate_access_log;

